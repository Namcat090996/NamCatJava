    private void btnChuyenDoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChuyenDoiActionPerformed
	//Declare variables
	String strSoThapPhan = "", strSoNhiPhan = "";
	int soThapPhan = 0, soDu = 0;
	
	//Declare array
	List<String> lstBIT = new ArrayList<String>();
	
	//Take user information entered from the keyboard and use Try-Catch, If to catch the error
	strSoThapPhan = txtSoThapPhan.getText();
	//If
	if(strSoThapPhan.length() == 0)
	{
	    JOptionPane.showMessageDialog(rootPane, "Bạn phải nhập giá trị số thập phân cần chuyển");
	}
	//Try-Catch
	try
	{
	    soThapPhan = Integer.parseInt(strSoThapPhan);
	}
	catch(NumberFormatException ex)
	{
	    System.err.println("Bạn phải nhập giá trị số thập phân là số nguyên. Chi tiết lỗi: " + ex.getMessage());
	    JOptionPane.showMessageDialog(rootPane,"Bạn phải nhập giá trị số thập phân là số nguyên");
	    txtSoThapPhan.requestFocus();
	    return;
	}

	//Use loop to convert Int to Binary
	while(soThapPhan > 0)
	{
	    soDu = soThapPhan%2;
	    soThapPhan /= 2;
	    lstBIT.add("" + soDu);
	}
	
	//Use loop to reverse the list[]
	for(int i = lstBIT.size()-1; i >= 0; i--)
	{
	    strSoNhiPhan += lstBIT.get(i);
	}
	
	//Print results
	txtSoNhiPhan.setText(strSoNhiPhan);
	txtSoNhiPhan.setEditable(false);
	
    }//GEN-LAST:event_btnChuyenDoiActionPerformed

