    private void btnChuyenDoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChuyenDoiActionPerformed
        //Declare variables
	String strDuLieu1 = "", strDuLieu2 = "", strChonDuLieu1 = "", strChonDuLieu2 = "";
	int duLieu1 = 0, duLieu2 = 0;	
	
	//Take user information entered from the keyboard
	strDuLieu1 = txtDuLieu1.getText();
	strChonDuLieu1 = "" + cboDuLieu1.getSelectedItem();
	strChonDuLieu2 = "" + cboDuLieu2.getSelectedItem();
		
	//Use Try-Catch to catch the error
	if(strDuLieu1.length() == 0)
	{
	    JOptionPane.showMessageDialog(rootPane, "Bạn không được để trống giá trị cần chuyển");
	    txtDuLieu1.requestFocus();
	    return;
	}
	try
	{
	    if(strChonDuLieu1.equals("Số thập phân") || strChonDuLieu1.equals("Số nhị phân") || strChonDuLieu1.equals("Số bát phân"))
	    {
		duLieu1 = Integer.parseInt(strDuLieu1);		
	    }
	}
	catch(NumberFormatException ex)
	{
	    System.err.println("Bạn phải nhập giá trị kiểu số. Chi tiết lỗi: " + ex.getMessage());
	    JOptionPane.showMessageDialog(rootPane, "Bạn phải nhập giá trị kiểu số");
	    txtDuLieu1.requestFocus();
	    return;
	}
	
	//Convert from Int to Binaray, Hexa, Octal
	if(strChonDuLieu1.equals("Số thập phân"))
	{
	    if(strChonDuLieu2.equals("Số nhị phân"))
	    {
		strDuLieu2 = Integer.toBinaryString(duLieu1);		
	    }
	    if(strChonDuLieu2.equals("Số bát phân"))
	    {
		strDuLieu2 = Integer.toOctalString(duLieu1);	
	    }
	    if(strChonDuLieu2.equals("Số thập lục phân"))
	    {
		strDuLieu2 = Integer.toHexString(duLieu1);	
	    }
	    if(strChonDuLieu2.equals("Số thập phân"))
	    {
		strDuLieu2 = strDuLieu1;	
	    }	    
	}
	
	//Convert from Binary to Int, Hexa, Octal
	if(strChonDuLieu1.equals("Số nhị phân"))
	{
	    if(strChonDuLieu2.equals("Số thập phân"))
	    {
		duLieu2 = Integer.parseInt(strDuLieu1, 2);
		strDuLieu2 = Integer.toString(duLieu2);
	    }
	    if(strChonDuLieu2.equals("Số bát phân"))
	    {
		duLieu2 = Integer.parseInt(strDuLieu1, 2);
		strDuLieu2 = Integer.toOctalString(duLieu2);
	    }
	    if(strChonDuLieu2.equals("Số thập lục phân"))
	    {
		duLieu2 = Integer.parseInt(strDuLieu1, 2);
		strDuLieu2 = Integer.toHexString(duLieu2);			
	    }
	    if(strChonDuLieu2.equals("Số nhị phân"))
	    {
		strDuLieu2 = strDuLieu1;	
	    }	    
	}
	
	//Convert from Octal to Binary, Int, Hexa
	if(strChonDuLieu1.equals("Số bát phân"))
	{
	    if(strChonDuLieu2.equals("Số thập phân"))
	    {
		duLieu2 = Integer.parseInt(strDuLieu1, 8);
		strDuLieu2 = Integer.toString(duLieu2);
	    }
	    if(strChonDuLieu2.equals("Số bát phân"))
	    {
		strDuLieu2 = strDuLieu1;
	    }
	    if(strChonDuLieu2.equals("Số thập lục phân"))
	    {
		duLieu2 = Integer.parseInt(strDuLieu1, 8);
		strDuLieu2 = Integer.toHexString(duLieu2);			
	    }
	    if(strChonDuLieu2.equals("Số nhị phân"))
	    {
		duLieu2 = Integer.parseInt(strDuLieu1, 8);
		strDuLieu2 = Integer.toBinaryString(duLieu2);			
	    }	    
	}
	
	//Convert from Hexa to Binary, Int, Octal
	if(strChonDuLieu1.equals("Số thập lục phân"))
	{
	    if(strChonDuLieu2.equals("Số thập phân"))
	    {
		duLieu2 = Integer.parseInt(strDuLieu1, 16);
		strDuLieu2 = Integer.toString(duLieu2);
	    }
	    if(strChonDuLieu2.equals("Số bát phân"))
	    {
		duLieu2 = Integer.parseInt(strDuLieu1, 16);
		strDuLieu2 = Integer.toOctalString(duLieu2);			
	    }
	    if(strChonDuLieu2.equals("Số thập lục phân"))
	    {
		strDuLieu2 = strDuLieu1;
					
	    }
	    if(strChonDuLieu2.equals("Số nhị phân"))
	    {
		duLieu2 = Integer.parseInt(strDuLieu1, 16);
		strDuLieu2 = Integer.toBinaryString(duLieu2);					
	    }	    
	}	
	txtDuLieu2.setText(strDuLieu2);
	txtDuLieu2.setEditable(false);		
	
    }//GEN-LAST:event_btnChuyenDoiActionPerformed

