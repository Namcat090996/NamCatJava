    private void btnXemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXemActionPerformed
        // TODO add your handling code here:
        //Declare variables
        String strGiaTri = "", kieu = "";
        int giaTri = 0, ketQua = 0, a =0;
        
        //Take user information entered from the keyboard
        strGiaTri = txtGiaTri.getText();
        
        //Convert String to int
        try
        {
            giaTri = Integer.parseInt(strGiaTri);
        }
        catch(NumberFormatException ex)
        {
            System.err.println("Bạn cần nhập giá trị là số nguyên. Chi tiết lỗi: " + ex.getMessage());
            JOptionPane.showMessageDialog(rootPane, "Bạn cần nhập giá trị là số nguyên");
            txtGiaTri.requestFocus();
            return;
        }

        if(giaTri%2 == 0)
        {
            kieu = "Chẵn";
            while(a <= giaTri)
            {
                System.out.print(a + " ");
                a = a + 2;
                ketQua = a;
            }
        }
        else
        {
            kieu = "Lẻ";
            while(a <= giaTri)
            {
                System.out.print(a + " ");
                a = a + 1;
                ketQua = a;                
            }
        }
        
        //Show results
        txtKetQua.setText("" + ketQua);
        txtKieu.setText("" + kieu);
        
    }//GEN-LAST:event_btnXemActionPerformed

