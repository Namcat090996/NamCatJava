    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
	//Khai báo biến
	String maSV = "", hoTen = "", dienThoai = "", email = "", diaChi = "", maKhoa = "";
	int gioiTinh = 0;
	
	//Lấy thông tin trên giao diện
	maSV = txtMaSV.getText().trim();
	hoTen = txtHoTen.getText().trim();
	dienThoai = txtDienThoai.getText().trim();
	email = txtEmail.getText().trim();
	diaChi = txtDiaChi.getText().trim();
	
	//Chọn giới tính
	if(rbtnNu.isSelected())//Nữ
	{
	    gioiTinh = 1;
	}	

	//Chọn object khoa từ combobox để lưu vào database
	ChuyenKhoa objKhoa = (ChuyenKhoa)cboChuyenKhoa.getSelectedItem();
	
	//Hiển thị mã khoa trong bảng danh sách sinh viên
	if(objKhoa != null)
	{
	    maKhoa = objKhoa.getMaKhoa().trim();	    
	}
	
	//Bắt lỗi khi người dùng không nhập đủ thông tin 	
	//Mã sinh viên
	if(maSV.length() == 0)
	{
            JOptionPane.showMessageDialog(rootPane, "Bạn cần nhập mã sinh viên");
            txtMaSV.requestFocus();
            return;	    
	}
	
	//Họ tên sinh viên
	if(hoTen.length() == 0)
	{
            JOptionPane.showMessageDialog(rootPane, "Bạn cần nhập họ tên sinh viên");
            txtHoTen.requestFocus();
            return;	    
	}
	//Kiểm tra họ tên nếu chứa chữ số
	char hoTenCheck[] = hoTen.toCharArray();
	
	for(char giaTri: hoTenCheck)
	{
	    if(Character.isDigit(giaTri))
	    {
		JOptionPane.showMessageDialog(rootPane, "Tên không hợp lệ");
		txtHoTen.requestFocus();
		return;
	    }	    
	}
	
	//Giới tính
	if(!rbtnNam.isSelected() && !rbtnNu.isSelected())
	{
            JOptionPane.showMessageDialog(rootPane, "Bạn cần chọn giới tính sinh viên");
	    rbtnNam.requestFocus();
            return;	    
	}
	
	//Điện thoại
	if(dienThoai.length() == 0)
	{
            JOptionPane.showMessageDialog(rootPane, "Bạn cần nhập số điện thoại sinh viên");
            txtDienThoai.requestFocus();
            return;	    
	}
	if(dienThoai.contains(" "))
	{
	    JOptionPane.showMessageDialog(rootPane, "Số điện thoại không hợp lệ");
	    txtDienThoai.requestFocus();
	    return;	    
	}
	//Kiểm tra nếu điện thoại chứa kí tự chữ cái
	char dienThoaiCheck[] = dienThoai.toCharArray();
	
	for(char giaTri: dienThoaiCheck)
	{
	    if(Character.isLetter(giaTri))
	    {
		JOptionPane.showMessageDialog(rootPane, "Số điện thoại không hợp lệ");
		txtDienThoai.requestFocus();
		return;
	    }	    
	}
	
	//Email
	if(email.length() == 0)
	{
            JOptionPane.showMessageDialog(rootPane, "Bạn cần nhập email sinh viên");
            txtEmail.requestFocus();
            return;	    
	}	
	
	//Địa chỉ
	if(diaChi.length() == 0)
	{
            JOptionPane.showMessageDialog(rootPane, "Bạn cần nhập địa chỉ sinh viên");
            txtDiaChi.requestFocus();
            return;	    
	}
	
	//Khởi tạo đối tượng để gán thông tin mới/chỉnh sửa
	SinhVien objSV = new SinhVien();
	
	//Gán giá trị cho object
	objSV.setMaSV(maSV);
	objSV.setHoTen(hoTen);
	objSV.setGioiTinh(gioiTinh);
	objSV.setNgaySinh(jDateChooserNgaySinh.getDate());
	objSV.setDienThoai(dienThoai);
	objSV.setEmail(email);
	objSV.setDiaChi(diaChi);
	objSV.setMaKhoa(maKhoa);
	
	//Khai báo đối tượng để gọi hàm cập nhật/thêm mới
	SinhVienBus sinhVienBus = new SinhVienBus();
	
	//Khai báo kết quả trả về và kiểm tra trùng mã sinh viên
	boolean ketQua, kiemTraTrung; 
	
	//Gọi hàm kiểm tra trùng mã sinh viên
	kiemTraTrung = sinhVienBus.kiemTraTrungMaSV(maSV);
	
	if(!this.maSV.isEmpty())//TH sửa
	{
	    ketQua = sinhVienBus.capNhatSV(objSV);
            if(ketQua)
            {
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thông tin sinh viên thành công");
            }            	    
	}
	else//TH thêm mới
	{
	    if(kiemTraTrung == true)//Nếu mã sinh viên thêm mới không trùng mã sinh viên với danh sách đã có
	    {
		ketQua = sinhVienBus.themMoiSV(objSV);
                if(ketQua)
                {
                    JOptionPane.showMessageDialog(rootPane, "Thêm mới thông tin sinh viên thành công");	
                }                  		
	    }
	    else
	    {
		JOptionPane.showMessageDialog(rootPane, "Mã sinh viên này đã tồn tại. Vui lòng nhập lại mã sinh viên");
		txtMaSV.requestFocus();
		return;		
	    }
	}
	
	if(ketQua)
	{
	    fmDanhSachSV.hienThiDanhSachSinhVien();
	}
	
    }//GEN-LAST:event_btnCapNhatActionPerformed

