    /**
     * Xử lý chương trình tính toán
     * @param evt 
     */
    private void btnTinhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTinhToanActionPerformed
        //Khai báo biến
        String strSoa = "", strSob = "", phepTinh = "";
        double soa = 0, sob = 0, ketQua = 0;
        
        //Lấy thông tin trên giao diện
        strSoa = txtSoa.getText();
        strSob = txtSob.getText();
        phepTinh = "" + cboPhepTinh.getSelectedItem();
        
        //Chuyển về dạng số
        soa = Double.parseDouble(strSoa);
        sob = Double.parseDouble(strSob);
        
        //Xử lý tính toán
        if(phepTinh.equals("Cộng"))
        {
            ketQua = soa + sob;
        }
        
        if(phepTinh.equals("Trừ"))
        {
            ketQua = soa - sob;
        }
        
        if(phepTinh.equals("Nhân"))
        {
            ketQua = soa * sob;
        }
        
        if(phepTinh.equals("Chia"))
        {
            if(sob != 0)
            {
                ketQua = soa / sob;
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Bạn cần phải nhập số b khác 0");
                txtSob.requestFocus();
                return;
            }
        }
         
        //Hiển thị kết quả
        txtKetQua.setText("" + ketQua);
        
    }//GEN-LAST:event_btnTinhToanActionPerformed

