    private void btnChonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChonActionPerformed
        //Declare an object to choose files
	JFileChooser fileChooser = new JFileChooser();
	
	//Declare file path
	String duongDan = "";
	
	//Assign the file path to the interface
	if(fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION)
	{
	    duongDan = fileChooser.getSelectedFile().getAbsolutePath();
	    
	    txtDuongDan.setText(duongDan);
	    txtDuongDan.setEditable(false);
	}
    }//GEN-LAST:event_btnChonActionPerformed

    private void btnDocFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocFileActionPerformed
	//Catch the error when file cannot be found
	if(txtDuongDan.getText().length() == 0)
	{
	    JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn file cần đọc dữ liệu");
	    btnChon.requestFocus();
	    return;
	}

	//Declare the file path and file data
	String duongDan = "", noiDung = "";
	
	//Get file path from the interface
	duongDan = txtDuongDan.getText();
	
	//Call the function to read file data
	noiDung = FileSupport.docNoiDungFile(duongDan);
	
	//Assign the data to the interface
	txtNoiDung.setText(noiDung);
    }//GEN-LAST:event_btnDocFileActionPerformed

    private void btnGhiFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGhiFileActionPerformed
	//Declare file path and file data
	String noiDung = "", duongDan = "";
	
	//Get data from the interface
	noiDung = txtNoiDung.getText();
	
	//Declare an object to choose files
	JFileChooser fileChooser = new JFileChooser();
	
	if(fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION)
	{
	    //Get the file path
	    duongDan = fileChooser.getSelectedFile().getAbsolutePath();
	    
	    //Call the function to write file data
	    FileSupport.ghiDuLieuRaFile(noiDung, duongDan);
	    
	    JOptionPane.showMessageDialog(rootPane, "Ghi dữ liệu ra file thành công !");
	}

    }//GEN-LAST:event_btnGhiFileActionPerformed

