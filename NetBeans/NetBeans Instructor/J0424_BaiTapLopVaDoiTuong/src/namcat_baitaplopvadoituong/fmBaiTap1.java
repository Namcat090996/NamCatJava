    private void btnTinhToanHCNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTinhToanHCNActionPerformed
        //Declare variables
	double dai = 0, rong = 0, chuViHCN = 0, dienTichHCN = 0;
	
	//Take user information entered from the keyboard and catch the error with try-catch, if-else
	if(txtChieuDaiHCN.getText().length() == 0 || txtChieuRongHCN.getText().length() == 0)
	{
	    JOptionPane.showMessageDialog(rootPane, "Bạn cần phải nhập chiều dài và chiều rộng hình chữ nhật");
	    return;
	}
	
	try {
	    dai = Double.parseDouble(txtChieuDaiHCN.getText());
	    rong  = Double.parseDouble(txtChieuRongHCN.getText());
	} catch (NumberFormatException e) {
	    System.err.println("Chiều dài và chiều rộng phải là giá trị kiểu số. Chi tiết lỗi:" + e.getMessage());
	    JOptionPane.showMessageDialog(rootPane, "Chiều dài và chiều rộng phải là giá trị kiểu số");
	    return;
	}
	
	//Call the calculation function
	chuViHCN = TinhToan.chuViHCN(dai, rong);
	dienTichHCN = TinhToan.dienTichHCN(dai, rong);
	
	//Show the results
	txtChuViHCN.setText("" + chuViHCN);
	txtDienTichHCN.setText("" + dienTichHCN);
	
    }//GEN-LAST:event_btnTinhToanHCNActionPerformed

