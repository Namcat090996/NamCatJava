public class TinhToan {
    
    /**
     * Hàm tính tổng hai số thực
     * Overload
     * @param a
     * @param b
     * @return 
     */
    public static double tongHaiSo(double a, double b)
    {
	return a+b;
    }
    
    /**
     * Hàm tính tổng hai số nguyên
     * Overload
     * @param a
     * @param b
     * @return 
     */
    public static int tongHaiSo(int a, int b)
    {
	return a+b;
    }
    
    
}
