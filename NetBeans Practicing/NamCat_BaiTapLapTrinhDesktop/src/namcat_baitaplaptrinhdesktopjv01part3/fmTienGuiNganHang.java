    private void btnTinhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTinhToanActionPerformed
        // TODO add your handling code here:
	//Declare variables
	String strTienGui = "", strLaiSuat = "", strSoThang = "", strTongLai = "", strTongNhan = "";
	double tienGui = 0, laiSuat = 0, tongLai = 0, tongNhan = 0; int soThang = 0;
	
	//Take user information entered from the keyboard
	strTienGui = txtTienGui.getText();
	strLaiSuat = txtLaiSuat.getText();
	strSoThang = txtSoThang.getText();
	
	//Convert String to Double and use Try - Catch, If - Else to catch the error
	//Tiền gửi
	if(strTienGui.length() == 0)
	{
	    JOptionPane.showMessageDialog(rootPane, "Bạn phải nhập số tiền gửi");
	    txtTienGui.requestFocus();
	    return;
	}
	try
	{
	    tienGui = Double.parseDouble(strTienGui);
	}
	catch(NumberFormatException ex)
	{
	    System.err.println("Bạn phải nhập tiền gửi là giá trị số. Chi tiết lỗi: " + ex.getMessage());
	    JOptionPane.showMessageDialog(rootPane, "Bạn phải nhập tiền gửi là giá trị số. Chi tiết lỗi");
	    txtTienGui.requestFocus();
	    return;
	}
	//Lãi suất
	if(strLaiSuat.length() == 0)
	{
	    JOptionPane.showMessageDialog(rootPane, "Bạn phải nhập lãi suất/tháng");
	    txtLaiSuat.requestFocus();
	    return;
	}
	try
	{
	    laiSuat = Double.parseDouble(strLaiSuat);
	}
	catch(NumberFormatException ex)
	{
	    System.err.println("Bạn phải nhập lãi suất/tháng là giá trị số. Chi tiết lỗi: " + ex.getMessage());
	    JOptionPane.showMessageDialog(rootPane, "Bạn phải nhập lãi suất/tháng là giá trị số. Chi tiết lỗi");
	    txtLaiSuat.requestFocus();
	    return;
	}	
	
	//Số tháng gửi
	if(strSoThang.length() == 0)
	{
	    JOptionPane.showMessageDialog(rootPane, "Bạn phải nhập số tháng gửi");
	    txtSoThang.requestFocus();
	    return;
	}
	try
	{
	    soThang = Integer.parseInt(strSoThang);
	}
	catch(NumberFormatException ex)
	{
	    System.err.println("Bạn phải nhập số tháng gửi là giá trị số. Chi tiết lỗi: " + ex.getMessage());
	    JOptionPane.showMessageDialog(rootPane, "Bạn phải nhập số tháng gửi là giá trị số. Chi tiết lỗi");
	    txtSoThang.requestFocus();
	    return;
	}	
	
	//Computational processing
	for(int n = 1; n <= soThang; n++)
	{
	    
	    tongLai = tienGui*(laiSuat/100);
	    tienGui += tongLai;
	}

	tongNhan = tienGui + tongLai;
	
	//Convert to Currency Unit
	NumberFormat currentLocale = NumberFormat.getInstance();
	String str1 = currentLocale.format(tongLai);
	String str2 = currentLocale.format(tongNhan);
	
	//Print results
	txtTongLai.setText(str1);
	txtTongNhan.setText(str2);
	
    }//GEN-LAST:event_btnTinhToanActionPerformed

