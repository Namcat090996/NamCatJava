    private void btnTinhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTinhToanActionPerformed
        // TODO add your handling code here:
	//Declare variables
	long giaTri = 0, ketQua = 1;
	String strGiaTri = "";
	
	//Take user information entered from the keyboard
	strGiaTri = txtGiaTri.getText();
	
	//Use if to request user enter the value
	if(strGiaTri.length() == 0)
	{
	    JOptionPane.showMessageDialog(rootPane, "Bạn phải nhập giá trị cần tính toán");
	    txtGiaTri.requestFocus();
	    return;
	}	
	
	//Use Try-Catch to catch the error
	try
	{
	    giaTri = Long.parseLong(strGiaTri);
	}
	catch(NumberFormatException ex)
	{
	    System.err.println("Bạn phải nhập giá trị là kiểu số nguyên. Chi tiết lỗi: " + ex.getMessage());
	    JOptionPane.showMessageDialog(rootPane, "Bạn phải nhập giá trị là kiểu số nguyên");
	    txtGiaTri.requestFocus();
	    return;
	}
	
	//Use if to request user enter the positive value
	if(giaTri < 0)
	{
	    JOptionPane.showMessageDialog(rootPane, "Bạn phải nhập giá trị là kiểu số nguyên dương");
	    txtGiaTri.requestFocus();
	    return;
	}	

	
	//Computational processing
	for(long i = giaTri; i >= 1; i--)
	{
	    ketQua *= i;
	}
	
	//Show results
	txtKetQua.setText("" + ketQua);
	
    }//GEN-LAST:event_btnTinhToanActionPerformed

